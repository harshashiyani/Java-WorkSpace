package com.c169;

public class StartWith {
	public static void main(String[] args) {
		String str="Red is my favourite color";
		String str1="Orange is my favourite color";
		System.out.println(str+ ": Starts with Red? "+str.startsWith("Red"));
		System.out.println(str1+ ": Starts with Red? "+str1.startsWith("Red"));
	}

}
